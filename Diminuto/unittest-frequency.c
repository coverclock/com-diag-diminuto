#include "com/diag/diminuto/diminuto_unittest.h"
#include "diminuto_frequency.h"
#include <stdio.h>
#include <errno.h>

int main(int argc, char ** argv)
{

	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(COM_DIAG_DIMINUTO_FREQUENCY, 1) == 1);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(COM_DIAG_DIMINUTO_FREQUENCY, 1000) == 1000);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(COM_DIAG_DIMINUTO_FREQUENCY, 1000000) == 1000000);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(COM_DIAG_DIMINUTO_FREQUENCY, 1000000000) == 1000000000);

	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(1, 1) == COM_DIAG_DIMINUTO_FREQUENCY);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(1000, 1000) == COM_DIAG_DIMINUTO_FREQUENCY);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(1000000, 1000000) == COM_DIAG_DIMINUTO_FREQUENCY);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(1000000000, 1000000000) == COM_DIAG_DIMINUTO_FREQUENCY);

	/*
	 * If we ever change the duration of a tick, these tests will have to be
	 * changed.
	 */

	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(250, 1000000000) == 250000);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(250, 1000000) == 250);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(250, 1000) == 0);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_FROM(250, 1) == 0);

	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(250, 1000000000) == 0);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(250, 1000000) == 250);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(250, 1000) == 250000);
	ASSERT(COM_DIAG_DIMINUTO_TICKS_TO(250, 1) == 250000000);

    EXIT();
}
